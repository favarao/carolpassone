            <h1 class="page-title text-center my-4">NR 09 - PPRA</h1>

            <p>
            De acordo com a NR 09, a implantação do PPRA é obrigatória para todos os empregadores e instituições que admitam trabalhadores como empregados. Não há que se levar em conta o grau de risco ou a quantidade de empregados.
            </p>

            <p>
            Por exemplo, uma oficina mecânica, uma loja de lingerie ou uma indústria de produtos químicos, todos estão obrigados a ter um PPRA, cada um com sua característica e complexidade diferentes, e com seu número de colaboradores diferentes, seja este 1 ou seja 500.
            </p>

            <p>
            É um documento de ação contínua, um programa de gerenciamento de riscos do ambiente do trabalho, com ações a serem diariamente observadas, de modo que garanta a preservação da saúde e integridade dos trabalhadores frente a esses riscos do ambiente de trabalho a que estão diariamente expostos.
            </p>

            <p>
            Estes riscos podem ser compostos por agentes ergonômicos, físicos, químicos e biológicos existentes nos ambientes de trabalho, em função de sua natureza, concentração ou intensidade e tempo de exposição, e são capazes de causar danos à saúde dos trabalhadores.
            </p>


            <p class="pb-0">
            Agentes agressores a serem observados:
            </p>
            <ul class="objetivos">
                <li><strong>Físicos:</strong> ruído, vibrações, pressões anormais, temperaturas extremas, radiações ionizantes e radiações não ionizantes</li>
                <li><strong>Ergonômicos:</strong> postura inadequada, equipamento mal projetado que exige postura inadequada, espaço que impõe má postura para desenvolver a função, etc</li>
                <li><strong>Químicos:</strong> poeiras, fumos, névoas, neblinas, gases, vapores, absorvidos pelo organismo humano por via respiratória, através da pele ou por ingestão</li>
                <li><strong>Biológicos:</strong> bactérias, fungos, bacilos, parasitas, protozoários, vírus, dentre outros</li>
            </ul>

            <p><strong>Importante observação:</strong>  PPRA e o PCMSO - Ambos são programas de caráter permanente, coexistem nas empresas e instituições, com as fases de implementação definidas.</p>

            <p class="pb-0">Assim temos na NR-7, item 7.2.4:</p>
            <p>“O PCMSO deverá ser planejado e implantado com base nos riscos à saúde dos trabalhadores, especialmente os identificados nas avaliações previstas nas demais NR."</p>

            <p class="pb-0">Bem como a NR09 item 9.1.3 assim determina:</p>
            <p>"O PPRA é parte integrante do conjunto mais amplo das iniciativas da empresa no campo da preservação da saúde e da integridade dos trabalhadores, devendo estar articulado com o disposto nas demais NR, em especial com o Programa de Controle Médico de Saúde Ocupacional - PCMSO previsto na NR-7."</p>

            <p>Ou seja, o PPRA serve de subsídio ao PCMSO, e o PCMSO cumpre o que foi determinado no PPRA, assim, um sem o outro é inútil, e com a falta de um o outro não cumpre o que exige a Legislação.</p>
            
